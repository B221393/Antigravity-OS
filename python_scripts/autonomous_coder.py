def update_inbox(task_type, subject, file_name, reason=""):
    now = datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')
    entry = f"- [{now}] {task_type}: {subject} ({file_name}) | Reason: {reason}\n"
    
    with open(INBOX_FILE, "a", encoding="utf-8") as f:
        f.write(entry)

